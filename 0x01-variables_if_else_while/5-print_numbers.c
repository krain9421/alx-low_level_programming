#include <stdlib.h>
#include <stdio.h>
#include <time.h>
/**
* main - main function, program starts here
*
* Description: Prints all single digit Base10 numbers
* Return: 0 if program runs successfully
*/
int main(void)
{
	int i;

	for (i = 0; i < 10; i++)
	{
	printf("%d", i);
	}
	printf("\n");
	return (0);
}
